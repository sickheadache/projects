#include <ctype.h>
#include <stdio.h>

int ft_isdigit(int c) {
    
    if (c >= '0' && c <= '9')
        return 1;
    
        return 0;
}

int main() {
    
    printf("%d %d %d\n", ft_isdigit(3), ft_isdigit(48), ft_isdigit(57));
    printf("%d %d %d", isdigit(3), isdigit(48), isdigit(57));
    printf("\\");
}
      
                
                
                
                
                
                
              
                
                
                
                
                
                
                
                
                
                
                
                
                
                
                
                
                
                
                
                
                
                
                
                
                
                
                
                
                
                

    
        
    
    

